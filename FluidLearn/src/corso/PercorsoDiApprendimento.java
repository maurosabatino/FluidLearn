package corso;

public class PercorsoDiApprendimento {
	UnitaDA uda1;
	UnitaDA uda2;
	
	
	public PercorsoDiApprendimento(UnitaDA uda1, UnitaDA uda2) {
		this.uda1 = uda1;
		this.uda2 = uda2;
	}
	public UnitaDA getUda1() {
		return uda1;
	}
	public void setUda1(UnitaDA uda1) {
		this.uda1 = uda1;
	}
	public UnitaDA getUda2() {
		return uda2;
	}
	public void setUda2(UnitaDA uda2) {
		this.uda2 = uda2;
	}
	
}
